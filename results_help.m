function results_help(hObject, ~)

sz = get(groot, 'Screensize');
pos = [1 1 max([700, sz(3)/2]) sz(4)];

help_fig = figure('name', 'Results Help',...
    'Position', pos,...
    'NumberTitle', 'off',... %get rid of the 'Figure 1:'
    'MenuBar', 'none',... %remove 'File', 'Edit', etc. menu
    'ToolBar', 'none',... %remove icon based lower toolbar
    'Resize', 'on');

help_panel = uipanel('Parent', help_fig,...
    'Units', 'normalized',...
    'Position', [0.02 0.02 0.96 0.96],...
    'FontSize', 14);

annotation(help_panel, 'textbox', [0.02 0.02 0.96 0.96],...
    'String',...
        {'Clicking the ``Save Figure" button will save the entire GUI to a',...
        'Matlab .fig file in the same directory as the loaded data, which ',...
        'can later be loaded by simply opening it. The file will be called:',...
        '    saved-figure.fig',...
        '',...
        'Clicking the ``Save Image" button will save an image of the',...
        'results tab so that it can be printed. The image is saved to the',...
        'same directory as the loaded data, under the filename:',...
        '    saved-image.jpg',...
        '',...
        'Clicking the ``Save Results" button will write a text file to the',...
        'same directory as the data that was loaded, under the filename:',...
        '    saved-results.txt'},...
    'Interpreter', 'latex',...
    'FontSize', 16,...
    'VerticalAlignment', 'middle',...
    'HorizontalAlignment', 'center',...
    'EdgeColor', 'none');

end
